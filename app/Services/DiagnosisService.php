<?php

namespace App\Services;


/**
 * 診断ロジックをカプセル化したサービスクラス
 *
 * ユーザーの回答データを元に、減点方式でスコアを計算し、
 * 総合評価レベルやカテゴリごとのフィードバックコメントを生成します。
 */


class DiagnosisService
{
    /**
     * 診断スコアと各種フィードバックの計算を実行する
     */
    public function calculate(array $data): array
    {
        // 基本スコアを100点とし、懸念事項があるごとに減点していく方式を採用
        $score = 100;

        /*
        |--------------------------------------------------------------------------
        | カテゴリ別コメント格納用の配列
        |--------------------------------------------------------------------------
        */
        $environmentComments = []; // 住環境に関するフィードバック
        $timeComments = []; // 時間に関するフィードバック
        $costComments = []; // 費用に関するフィードバック

        /*
        |--------------------------------------------------------------------------
        | 住環境の評価
        |--------------------------------------------------------------------------
        */
        // ペット不可の物件に住んでいる場合は大幅減点（致命的な問題のため）
        if ($data['housing_type'] === 'ペット不可') {

            $score -= 30;

            $environmentComments[] =
                'ペット不可の住居では飼育が難しいため、住環境の見直しをおすすめします。';
        }

        // 動物病院へ連れて行ける環境にない場合
        if (!$data['can_visit_hospital']) {

            $score -= 15;

            $environmentComments[] =
                '通院体制に不安があります。近隣の動物病院や移動手段を確認しておきましょう。';
        }

        // 特に減点対象がない場合は、ポジティブなフィードバックを入れる
        if (empty($environmentComments)) {

            $environmentComments[] =
                '猫を迎えるための住環境は整っています。';
        }

        /*
        |--------------------------------------------------------------------------
        | 時間（ライフスタイル）の評価
        |--------------------------------------------------------------------------
        */
        // 留守時間が12時間以上の場合は大幅減点
        if ($data['absence_hours'] >= 12) {
            $score -= 20;
            $timeComments[] =
                '留守時間が長いため、猫が安心して過ごせる環境づくりが必要です。';
        // 留守時間が8〜11時間の場合はやや減点
        } elseif ($data['absence_hours'] >= 8) {
            $score -= 10;
            $timeComments[] =
                '留守時間がやや長めです。猫との時間を確保できるか検討しましょう。';
        }

        // 将来の特別なケア（介護や闘病）を受け入れられない場合
        if (!$data['accepts_special_care']) {
            $score -= 15;
            $timeComments[] =
                '高齢期や病気になった際の介護についても考えておくと安心です。';
        }

        if (empty($timeComments)) {
            $timeComments[] =
                '猫と過ごす時間を十分確保できそうです。';
        }

        /*
        |--------------------------------------------------------------------------
        | 費用の評価
        |--------------------------------------------------------------------------
        */
        // 1匹あたりの月間予算を算出して評価する（多頭飼育崩壊の防止）
        $budgetPerCat = $data['monthly_budget'] / $data['planned_cat_count'];

        // 1匹あたり5,000円未満は厳しいと判定
        if ($budgetPerCat < 5000) {
            $score -= 20;
            $costComments[] =
                '予算に余裕がない可能性があります。医療費や消耗品も考慮しましょう。';

        }
        // 1匹あたり10,000円未満はギリギリと判定
        elseif ($budgetPerCat < 10000) {
            $score -= 10;
            $costComments[] =
                '最低限の予算は確保できていますが、余裕があるとさらに安心です。';
        }

        if (empty($costComments)) {
            $costComments[] =
                '費用面の準備は十分です。';
        }

        /*
        |--------------------------------------------------------------------------
        | スコアの下限設定
        |--------------------------------------------------------------------------
        */
        // 減点が重なりマイナスになった場合でも、最低点は0点として扱う
        $score = max(0, $score);

        /*
        |--------------------------------------------------------------------------
        | 総合判定（レベル）
        |--------------------------------------------------------------------------
        | スコアの帯域ごとに、一言で表す準備度レベルを決定します。
        */
        $level = match (true) {
            $score === 100 => '最高',
            $score >= 80 => '高い',
            $score >= 60 => 'やや高い',
            $score >= 30 => '低い',
            default => 'かなり低い',
        };

        /*
        |--------------------------------------------------------------------------
        | 総評（サマリーテキスト）
        |--------------------------------------------------------------------------
        | 結果画面のトップに表示される、ユーザーへ向けたメインメッセージを決定します。
        */
        $summary = match (true) {
            $score === 100 =>
                '猫を迎える準備がしっかり整っています！' . "\n". 'この調子で素敵な猫ライフを始めましょう。',
            $score >= 60 =>
                '猫を迎える準備は概ね整っています。' . "\n". 'より安心して迎えられるよう、気になる項目も確認してみましょう。',
            $score >= 30 =>
                '猫を迎える前に、いくつか見直しておきたいポイントがあります。' . "\n". '下記の評価内容を参考に、準備を進めていきましょう。',
            default =>
                '現在の状況では、猫を迎える前に準備が必要な項目が多くあります。' . "\n". '猫との暮らしを安心して始めるために、まずは下記の内容を確認してみましょう。',
        };

        // 計算した全てのデータを連想配列にまとめて返却する
        return [
            'score' => $score,
            'level' => $level,
            'summary' => $summary,
            'category_comments' => [
                'environment' => $environmentComments,
                'time' => $timeComments,
                'cost' => $costComments,
            ],
        ];
    }
}